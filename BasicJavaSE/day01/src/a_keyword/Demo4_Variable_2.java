package a_keyword;

public class Demo4_Variable_2 {
    public static void main(String[] args) {
        int a = 10;
        a = a + 1;
        System.out.println(a);

        double d = 3.14;
        System.out.println(d);

        char c = 'a';
        System.out.println(c);

        String str = "abc";
        System.out.println(str);

        boolean b1 = false;
        System.out.println(b1);

        boolean b2 = true;
        System.out.println(b2);
    }
}
